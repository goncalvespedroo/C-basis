#include <iostream>
using namespace std;
int main(){
    
    int x; //declaramos a variavel
    x = 5; //atribuimos um valor à variavel
    double y = 6.7; //declaração e atribuição ao mesmo tempo. Foi usada uma tipagem diferente para suportar decimal
    double soma = x + y;

    cout << x << endl;
    cout << y << endl;
    cout << soma << endl;

    return 0;

}

/*
int = numeros inteiros
char = um unico caracter
bool = true ou false
string = varios caracteres
double = numeros inteiros e decimais
void = sem tipo
*/